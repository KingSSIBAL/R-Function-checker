# INSTRUCTOR FUNCTION
# =============================================================================

factorial <- function(n) {
  if (n < 0) stop("n must be non-negative")
  if (n == 0 || n == 1) return(1)
  result <- 1
  for (i in 2:n) result <- result * i
  result
}

